// 状态特效覆盖层
function getStateEffects(state: AgentState): [number, number, number][] {
  switch (state) {
    case 'thinking':
      // 思考气泡 ···
      return [[25,0,6],[27,1,6],[29,0,6],[26,1,6],[28,1,6]];
    case 'tool_use':
      // 火花 ✦ 两侧
      return [
        [0,15,8],[1,14,8],[1,16,8],[2,15,8],
        [29,15,8],[30,14,8],[30,16,8],[31,15,8],
      ];
    case 'responding':
      // 音符 ♪
      return [[26,0,6],[27,0,6],[27,1,6],[26,2,6],[25,3,6]];
    case 'error':
      // 警告 !
      return [[27,0,9],[27,1,9],[27,3,9],[26,0,9],[28,0,9]];
    default:
      return [];
  }
}

// 调色板
function getPalette(accent: string): Record<number, string> {
  return {
    1:  '#2D1600',  // 轮廓
    2:  '#FACC15',  // 壳
    3:  '#FEF08A',  // 壳亮
    4:  '#B45309',  // 壳暗
    5:  '#FEF3C7',  // 肚皮
    6:  '#FFFFFF',  // 白
    7:  '#111827',  // 瞳孔
    8:  accent,     // 角色色
    9:  '#FB7185',  // 腮红
    10: '#93C5FD',  // 眼光
  };
}

export default function LobsterAvatar({
  role,
  state,
  animated = false,
  size = 96,
}: {
  role: AgentRole;
  state: AgentState;
  animated?: boolean;
  size?: number;
}) {
  const roleConfig = ROLE_MAP[role];
  const palette = getPalette(roleConfig.accent);
  const motionClass = animated
    ? { idle: 'lobster-float', thinking: 'lobster-think', tool_use: 'lobster-work', responding: 'lobster-respond', error: 'lobster-error' }[state]
    : '';

  // 合并：基础 + 配件 + 状态特效
  const sprite = BASE.map(row => [...row]);
  for (const [x, y, c] of ROLE_ACCESSORIES[role] ?? []) {
    if (y >= 0 && y < 32 && x >= 0 && x < 32) sprite[y][x] = c;
  }
  for (const [x, y, c] of getStateEffects(state)) {
    if (y >= 0 && y < 32 && x >= 0 && x < 32) sprite[y][x] = c;
  }

  // 渲染像素
  const rects: React.ReactElement[] = [];
  for (let y = 0; y < 32; y++) {
    for (let x = 0; x < 32; x++) {
      const c = sprite[y][x];
      if (c > 0 && palette[c]) {
        rects.push(<rect key={`${x}-${y}`} x={x} y={y} width={1} height={1} fill={palette[c]} />);
      }
    }
  }

  return (
    <div
      className={`relative ${motionClass}`}
      style={{ width: size, height: size, filter: `drop-shadow(0 4px 12px ${roleConfig.softAccent})`, imageRendering: 'pixelated' }}
    >
      <svg viewBox="0 0 32 32" className="h-full w-full" style={{ imageRendering: 'pixelated' }} shapeRendering="crispEdges">
        {rects}
      </svg>
    </div>
  );
}
